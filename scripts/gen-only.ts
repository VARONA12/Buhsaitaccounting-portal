async function generateWithRetry(topic: { title: string; prompt: string }, attempt = 1): Promise<string> {
  try {
    const controller = new AbortController();
    const timeout = setTimeout(() => controller.abort(), 120000);

    const res = await fetch(`${DEEPSEEK_BASE_URL}/chat/completions`, {
      method: "POST",
      headers: { "Content-Type": "application/json", Authorization: `Bearer ${DEEPSEEK_API_KEY}` },
      body: JSON.stringify({ model: "deepseek-chat", messages: [{ role: "user", content: topic.prompt }], max_tokens: 3000, temperature: 0.7 }),
      signal: controller.signal,
    });
    clearTimeout(timeout);

    if (!res.ok) throw new Error(`HTTP ${res.status}: ${await res.text()}`);
    const data = await res.json();
    return data.choices?.[0]?.message?.content ?? "";
  } catch (err) {
    if (attempt < 3) {
      console.log(`  ⟳ Попытка ${attempt + 1}...`);
      await new Promise((r) => setTimeout(r, 5000 * attempt));
      return generateWithRetry(topic, attempt + 1);
    }
    throw err;
  }
}

async function main() {
  const OUTPUT = "scripts/articles.json";
  let saved: any[] = [];

  if (fs.existsSync(OUTPUT)) {
    saved = JSON.parse(fs.readFileSync(OUTPUT, "utf8"));
    console.log(`📂 Продолжаю с ${saved.length} уже готовых статей\n`);
  }

  const savedTitles = new Set(saved.map((a: any) => a.title));

  for (const topic of TOPICS) {
    if (savedTitles.has(topic.title)) {
      console.log(`⏭️  Пропускаю: ${topic.title}`);
      continue;
    }

    console.log(`\n📝 Генерирую: ${topic.title}`);
    try {
      const content = await generateWithRetry(topic);
      console.log(`✅ Готово (${content.length} символов)`);

      saved.push({ title: topic.title, excerpt: topic.excerpt, content, author: "Анна Туманян" });
      fs.writeFileSync(OUTPUT, JSON.stringify(saved, null, 2));
      console.log(`💾 Сохранено в ${OUTPUT}`);

      await new Promise((r) => setTimeout(r, 3000));
    } catch (err: any) {
      console.error(`❌ Ошибка: ${err.message}`);
    }
  }

  console.log(`\n✅ Готово! Итого: ${saved.length} статей в ${OUTPUT}`);
}

main();
