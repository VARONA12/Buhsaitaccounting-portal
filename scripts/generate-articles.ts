async function generateArticle(topic: { title: string; prompt: string }) {
  console.log(`\n📝 Генерирую: ${topic.title}`);

  const res = await fetch(`${DEEPSEEK_BASE_URL}/chat/completions`, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Authorization: `Bearer ${DEEPSEEK_API_KEY}`,
    },
    body: JSON.stringify({
      model: "deepseek-chat",
      messages: [{ role: "user", content: topic.prompt }],
      max_tokens: 4000,
      temperature: 0.7,
    }),
  });

  if (!res.ok) {
    const err = await res.text();
    throw new Error(`DeepSeek error: ${res.status} — ${err}`);
  }

  const data = await res.json();
  const content = data.choices?.[0]?.message?.content ?? "";
  console.log(`✅ Готово (${content.length} символов)`);
  return content;
}

async function main() {
  console.log("🚀 Генерация статей для ЭлитФинанс через DeepSeek\n");

  for (const topic of TOPICS) {
    try {
      const existing = await db.article.findFirst({ where: { title: topic.title } });
      if (existing) {
        console.log(`⏭️  Пропускаю (уже есть): ${topic.title}`);
        continue;
      }

      const content = await generateArticle(topic);

      await db.article.create({
        data: {
          title: topic.title,
          content,
          excerpt: topic.excerpt,
          author: "Анна Туманян",
          published: true,
        },
      });

      console.log(`💾 Сохранено: ${topic.title}`);
      await new Promise((r) => setTimeout(r, 1500));
    } catch (err) {
      console.error(`❌ Ошибка для "${topic.title}":`, err);
    }
  }

  console.log("\n✅ Готово!");
  await db.$disconnect();
}

main();
